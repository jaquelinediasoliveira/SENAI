package model;

public class JogoDoBicho {
	private int id;
	private String bicho;
	private String dataJogo;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getBicho() {
		return bicho;
	}
	public void setBicho(String bicho) {
		this.bicho = bicho;
	}
	public String getDataJogo() {
		return dataJogo;
	}
	public void setDataJogo(String dataJogo) {
		this.dataJogo = dataJogo;
	}
	
}
